namespace Domain.Enums;

/// <summary>
/// Represents the different types of vehicles.
/// </summary>
public enum VehicleType
{
    /// <summary>
    /// A car designed for transporting passengers.
    /// </summary>
    Car,

    /// <summary>
    /// A motorized two-wheeled vehicle.
    /// </summary>
    Motorcycle,

    /// <summary>
    /// A truck designed for carrying goods or materials.
    /// </summary>
    Truck,

    /// <summary>
    /// A bus used for carrying passengers in larger groups.
    /// </summary>
    Bus,

    /// <summary>
    /// A vehicle with tracks used for off-road or heavy-duty tasks.
    /// </summary>
    Tractor,

    /// <summary>
    /// A small, often electric vehicle used in urban areas.
    /// </summary>
    Scooter,

    /// <summary>
    /// A vehicle designed for transporting goods in a container (large cargo).
    /// </summary>
    ContainerTruck,

    /// <summary>
    /// A vehicle designed to carry or transport animals.
    /// </summary>
    AnimalTransport,

    /// <summary>
    /// A vehicle designed for off-road and all-terrain use.
    /// </summary>
    OffRoadVehicle,

    /// <summary>
    /// A specialized vehicle used in construction or demolition.
    /// </summary>
    ConstructionVehicle,

    /// <summary>
    /// A vehicle designed for public or private transport in water.
    /// </summary>
    Boat,

    /// <summary>
    /// A vehicle designed to transport goods or passengers through the air.
    /// </summary>
    Aircraft,

    /// <summary>
    /// A non-motorized vehicle, typically used for short-distance travel.
    /// </summary>
    Bicycle,
}
