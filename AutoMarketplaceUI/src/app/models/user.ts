/**
 * Describes a user.
 */
export interface User {
  /**
   * The email of the user.
   */
  email: string;

  /**
   * The name of the user.
   */
  name: string;

  /**
   * The surname of the user.
   */
  surname: string;

  /**
   * The phoneNumber of the user.
   */
  phoneNumber: string;

  /**
   * The token.
   */
  token: string;

  /**
   * The imgUrl of the user.
   */
  imgUrl: string;
}
